package myjava;

public class Operator_Assignment {

    public static void main(String args[]) {
        // Operator Assignment
       
        // Assignment Operator Penjumlahan
        int a = 100;
        a += 50;
        System.out.println("\n"+a+"\n"); // Hasil: 150
        
        // Assignment Operator Pengurangan 
        int b = 250;
        b -= 100;
        System.out.println(b+"\n"); // Hasil: 150
        
        // Assignment Operator Perkalian
        int c = 50;
        c *= 3;
        System.out.println(c+"\n"); // Hasil: 150
        
        // Assignment Operator Pembagian
        int d = 15000;
        d /= 150;
        System.out.println(d+"\n"); // Hasil: 100
        
        // Assignment Operator Modulus
        int e = 300;
        e %= 120;
        System.out.println(e+"\n"); // Hasil: 60
    }
}
